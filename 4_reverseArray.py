# Reverse Using reverse()

arr=[3,4,5,6,7,8]
print("arr= ",arr)
arr.reverse();
print("Reverse arr",arr)

# Reverse Using Slicing

MyList=[1,2,3,4]
print("My list before reverse: ",MyList)
MyList2=MyList[::-1]   # : is used to slices starting,ending,-1 is reverse order
print("My list after reverse: ",MyList2)                       
